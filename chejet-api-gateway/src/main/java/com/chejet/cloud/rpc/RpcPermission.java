package com.chejet.cloud.rpc;

import java.io.Serializable;

/**
 * @author Neo.fang
 * @creatTime 2018/11/9 0009
 *
 * RPC回传权限对象(含菜单)
 */
public class RpcPermission implements Serializable {

    private static final long serialVersionUID = 6413358335961655343L;

    /** 功能名称 */
    private String funName;
    /** 功能CODE */
    private String funCode;
    /** 权限点名称 */
    private String pointName;
    /** 权限点CODE */
    private String pointCode;
    /** 权限URL */
    private String pointUrl;
    /** 是否菜单1是 0否 */
    private Integer isMenu;

    public String getFunName() {
        return funName;
    }

    public void setFunName(String funName) {
        this.funName = funName;
    }

    public String getFunCode() {
        return funCode;
    }

    public void setFunCode(String funCode) {
        this.funCode = funCode;
    }

    public String getPointName() {
        return pointName;
    }

    public void setPointName(String pointName) {
        this.pointName = pointName;
    }

    public String getPointCode() {
        return pointCode;
    }

    public void setPointCode(String pointCode) {
        this.pointCode = pointCode;
    }

    public String getPointUrl() {
        return pointUrl;
    }

    public void setPointUrl(String pointUrl) {
        this.pointUrl = pointUrl;
    }

    public Integer getIsMenu() {
        return isMenu;
    }

    public void setIsMenu(Integer isMenu) {
        this.isMenu = isMenu;
    }
}
