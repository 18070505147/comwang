package com.chejet.cloud.vo;

import java.util.List;

/**
 * @Author: Bean.Wang
 * @Date: 2018/12/27 14:42
 */
public class RoleParaVO {
    private List<FunctionPermissionPointVO> functionPermissionPointList;
    private List<SensitiveDataVO> sensitiveDataList;

    public RoleParaVO() {
    }

    public RoleParaVO(List<FunctionPermissionPointVO> functionPermissionPointList, List<SensitiveDataVO> sensitiveDataList) {
        this.functionPermissionPointList = functionPermissionPointList;
        this.sensitiveDataList = sensitiveDataList;
    }

    public List<FunctionPermissionPointVO> getFunctionPermissionPointList() {
        return functionPermissionPointList;
    }

    public void setFunctionPermissionPointList(List<FunctionPermissionPointVO> functionPermissionPointList) {
        this.functionPermissionPointList = functionPermissionPointList;
    }

    public List<SensitiveDataVO> getSensitiveDataList() {
        return sensitiveDataList;
    }

    public void setSensitiveDataList(List<SensitiveDataVO> sensitiveDataList) {
        this.sensitiveDataList = sensitiveDataList;
    }
}
