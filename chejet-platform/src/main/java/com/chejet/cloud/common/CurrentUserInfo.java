package com.chejet.cloud.common;

import java.util.List;

/**
 * @Description 登录用户信息
 * @Date 2018/12/10 10:55
 * @Version 1.0
 */
public class CurrentUserInfo {
    /**
     * 用户id
     */
    private Long UserId;
    /**
     * 用户名
     */
    private String userName;
    /**
     * 用户别名
     */
    private String aliasName;

    /**
     * 租户ID
     */
    private Long tenantId;
    /**
     * 角色类型
     * 1-PLATFORM_ADMIN-平台管理员,
     * 2-PLATFORM_OPERATOR-平台操作员,
     * 3-TENANT_ADMIN-租户管理员,
     * 4-COMPANY_ADMIN-企业管理员,
     * 5-TENANT_OPERATOR-租户操作员,
     * 0-NORMAL-普通角色
     */
    private Integer roleType;

    /**
     * 角色id
     */
    private Long roleId;

    /**
     * 角色名
     */
    private String roleName;

    /**
     * 用户权限列表
     */
    private List userPermissions;


    public Long getUserId() {
        return UserId;
    }

    public void setUserId(Long userId) {
        UserId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getAliasName() {
        return aliasName;
    }

    public void setAliasName(String aliasName) {
        this.aliasName = aliasName;
    }

    public Integer getRoleType() {
        return roleType;
    }

    public void setRoleType(Integer roleType) {
        this.roleType = roleType;
    }

    public Long getTenantId() {
        return tenantId;
    }

    public void setTenantId(Long tenantId) {
        this.tenantId = tenantId;
    }

    public Long getRoleId() {
        return roleId;
    }

    public void setRoleId(Long roleId) {
        this.roleId = roleId;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public List getUserPermissions() {
        return userPermissions;
    }

    public void setUserPermissions(List userPermissions) {
        this.userPermissions = userPermissions;
    }
}
