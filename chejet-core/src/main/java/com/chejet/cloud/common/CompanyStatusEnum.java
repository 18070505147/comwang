package com.chejet.cloud.common;

/**
 * @Description
 * @Date 2018/12/13 14:37
 * @Version 1.0
 */
public enum CompanyStatusEnum {
    DISENABLE(0, "禁用"),
    ENABLE(1, "启用");

    private int value;
    private String name;

    CompanyStatusEnum(int value, String name) {
        this.value = value;
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
